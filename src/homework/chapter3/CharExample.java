package homework.chapter3;

public class CharExample {
    public static void main(String[] args) {
        char ch1 = 88, ch2 = 'Y';
        System.out.println("ch1 and ch2: " + ch1 + " " + ch2);
        ch1++;
        System.out.println("Ch1 is now: " + ch1); //Ch1 is now Y

        char i = '\'';
        System.out.println(i);   //use \ for the special symbol

        char a = '"';
        System.out.println(a);
    }

}
